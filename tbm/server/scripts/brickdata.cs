package IceBlockStuff {
function StaticShape::delete(%this) {
if(%this.getDataBlock() == nameToID(staticIceBlock)) {
  %s = %this.getScale();
  %p = %this.getMountNodeObject(0);
  Parent::delete(%this);
  %p.setScale(%s);
}
else
  Parent::delete(%this);
}
};
